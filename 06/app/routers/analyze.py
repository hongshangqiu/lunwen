@router.post("/execute_fix", response_model=dict)
def execute_fix(code: str = Body(...), scene_id: str = Body("unknown")):
    """执行修复脚本"""
    import os
    import requests
    import io
    from contextlib import redirect_stdout
    
    try:
        timestamp = int(time.time())
        script_dir = "data/scripts"
        os.makedirs(script_dir, exist_ok=True)
        script_path = os.path.join(script_dir, f"fix_{scene_id}_{timestamp}.py")
        
        with open(script_path, "w", encoding="utf-8") as f:
            f.write("# Generated by LLM-Ops Copilot\n")
            f.write(code)
        
        exec_globals = {"requests": requests}
        f_out = io.StringIO()
        with redirect_stdout(f_out):
            exec(code, exec_globals)
        
        output = f_out.getvalue()
        
        return {
            "success": True,
            "message": f"修复脚本已生成并执行: {script_path}",
            "script_path": script_path,
            "output": output
        }
    except Exception as e:
        return {"success": False, "message": f"执行失败: {str(e)}"}
