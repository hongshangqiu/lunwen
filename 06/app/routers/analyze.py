@router.post("/analyze/rca", response_model=dict)
def analyze_rca(req: dict):
    """
    RCA 结构化分析接口
    """
    try:
        from app.services.rca_engine import RCAResult, RootCause
        from app.core.evidence_builder import EvidenceBuilder
        from app.services import knowledge_service, prometheus_service, log_service
        
        question = req.get("question", "")
        service = req.get("service", "default")
        time_range = req.get("time_range", "last_1h")
        
        # 1. 知识检索
        knowledge_results = knowledge_service.search_knowledge(question, service, top_k=3)
        
        # 2. 指标查询
        metrics_data = prometheus_service.summarize_metrics(
            service, "1h ago", "now",
            ["cpu_usage", "memory_usage", "db_connections"]
        )
        
        # 3. 日志查询
        log_lines = log_service.read_logs(service, "1h ago", "now", 
                                         ["error", "warning", "exception"], limit=20)
        logs_data = log_service.summarize_logs(log_lines)
        
        # 4. 构建上下文
        builder = EvidenceBuilder()
        context = builder.build(
            question=question,
            service=service,
            time_range=time_range,
            knowledge=knowledge_results,
            metrics=metrics_data,
            logs=logs_data
        )
        
        # 5. 执行 RCA 分析
        from app.services.rca_engine import RCAEngine
        engine = RCAEngine()
        result = engine.analyze(context)
        
        return {
            "success": True,
            "data": result.to_dict()
        }
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


