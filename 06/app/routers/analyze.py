"""
Analyze API Router - RAG Pipeline 集成
"""
from fastapi import APIRouter, HTTPException, Body
from pydantic import BaseModel
from typing import List, Optional, Dict
import time

router = APIRouter()


class RAGAnalyzeRequest(BaseModel):
    """RAG Pipeline 分析请求"""
    question: str
    service: Optional[str] = None
    time_range: str = "last_1h"
    metrics: Optional[List[str]] = None
    keywords: Optional[List[str]] = None


@router.post("/analyze/rag", response_model=dict)
def analyze_with_rag_pipeline(req: RAGAnalyzeRequest):
    """
    使用 RAG Pipeline 进行端到端分析
    
    完整方案：RAG + 实时数据 + 结构化 Prompt
    """
    try:
        from app.services.rag_pipeline import RAGPipeline
        
        pipeline = RAGPipeline()
        result = pipeline.analyze(
            question=req.question,
            service=req.service,
            time_range=req.time_range,
            metrics=req.metrics,
            keywords=req.keywords
        )
        
        return {
            "success": True,
            "data": result
        }
    except Exception as e:
        return {
            "success": False,
            "error": str(e)
        }


